#include<bits/stdc++.h>

vector<vector<int> > Solution::generateMatrix(int A) {
    vector<vector<int> > ans(A,vector<int> A);
    int m,n,k=0,l=0,i;
    count=1;
    m=A;
    n=A;
    while(k<m && l<n)
    {
        for(i=l;i<n;i++)
        {
            ans[k][i]=count;
            count++;
        }
        k++;
        
        for(i=k;i<m;i++)
        {
            ans[i][n-1]=count;
            count++;
        }
        n--;
        
        if(k<m)
        {
            for(i=n-1;i>=l;i--)
            {
                ans[m-1][i]=count;
                count++;
            }
            m--;
        }
        
        if(l<n)
        {
            for(i=m-1;i>=k;i--)
            {
                ans[i][l]=count;
                count++;
            }
            l++;
        }
        
    }
    return ans;
}





